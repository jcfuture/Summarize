package Summarize.JDBC;

public class JDBC_Spring_JDBCTemplte {
    /*
    * Spring JDBC
    *   Spring框架对JDBC进行见到封装，提供JDBCTemplte简化JDBC的开发
    *   步骤：
    *       1、导入jar包
    *       2、创建JDBCTemplte对象，依赖于数据源DataSource
    *           JdbcTemplte temple = new JdbcTemplte(ds);
    *       3、使用方法来（JdbcTemplte）来完成CRUD增删改查的操作
    *           update() 执行DML增删改语句
    *           queryFormap() 查询结果，将结果集封装为map集合，列为key值为value
    *           querylist()  查询结果，将结果集封装为list集合
    *           query()  查询结果，将结果集封装为JavaBean对象
    *           queryforObject() 查询结果，将结果集封装为对象*/
}
