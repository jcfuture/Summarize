public class DemoThedataTypeChainlist {
    /*数据结构-链表
      查询慢：链表中的地址不是连续的每次查询元素，都必须从头查询
      增删快：链表结构，增删一个元素，对链表的整体结构没有影响，所以增删快
      链表中的每一个元素也称之为一个节点，一个节点包含一个数据源（数组存储），两个指针域（存储地址）
      单向列表：链表中只有一个链子，不能保证元素的顺序（存储和取出元素顺序有可能不一致）
      双向列表：链表中有两条链子，有条链子是专门记录元素的顺序，是一个有序的集合*/
}
